        },
        methods:{
            /**
             * 点击【新增一级】
             */
            addLevel1() {
                let _this = this;
                _this.active = {};
                _this.level2 = [];
                _this.category = {
                    parent: "0"
                };
                $("#form-modal").modal("show");
            },

            /**
             * 点击【新增二级】
             */
            addLevel2() {
                let _this = this;
                if (Tool.isEmpty(_this.active)) {
                    Toast.warning("请先点击一级分类");
                    return;
                }
                _this.category = {
                    parent: _this.active.id
                };
                $(".modal").modal("show");
            },

            edit(category){
                let _this = this;
                _this.category = $.extend({},category);//对象复制
                $("#form-modal").modal("show")
            },
            all(){
                let _this = this;
                Loading.show();
                    _this.$ajax.post(process.env.VUE_APP_SERVER+"/business/category/all",{
                    }).then((response=>{
                        Loading.hide();
                        // console.log("查询章列表结果：",response);
                        let resp = response.data;
                        _this.categorys = resp.content;
                        // 将所有记录格式化成树形结构
                        _this.level1 = [];//格式化之前先将level1清空
                        for (let i = 0; i < _this.categorys.length; i++) {
                            let c = _this.categorys[i];
                            if (c.parent === '0') {
                                _this.level1.push(c);
                                for (let j = 0; j < _this.categorys.length; j++) {
                                    let child = _this.categorys[j];
                                    if (child.parent === c.id) {
                                        if (Tool.isEmpty(c.children)) {
                                            c.children = [];
                                        }
                                        c.children.push(child);
                                    }
                                }
                            }
                        }

                        _this.level2 = [];
                        // 对当前一级分类中选中的表格触发一次点击事件，以刷新二级菜单列表
                        // 注意：界面的渲染需要等vue绑定好变量后才做，所以加延时100ms
                        setTimeout(function () {
                            $("tr.active").trigger("click");
                        }, 100);
                    }))
            },
            save(){
                let _this = this;
                // 保存校验 TODO
                if (1 != 1
                    || !Validator.require(_this.category.parent, "父id")
                    || !Validator.require(_this.category.name, "名称")
                    || !Validator.length(_this.category.name, "名称", 1, 50)
                ) {
                    return;
                }

                Loading.show();
                _this.$ajax.post(process.env.VUE_APP_SERVER+"/business/category/save", _this.category).then((response=>{
                    Loading.hide();
                    // console.log("保存章列表结果：",response);
                    let resp = response.data;
                    if (resp.success){
                        $("#form-modal").modal("hide");
                        _this.all(1);
                        toast.success("保存成功")
                    }else{
                        toast.success(resp.message)
                    }
                }))
            },
            del(id){
                let _this = this;
                Confirm.show("删除章节不可恢复确认删除?",function (){
                    Loading.show();
                    _this.$ajax.delete(process.env.VUE_APP_SERVER+"/business/category/delete/"+id, _this.category).then((response=>{
                        Loading.hide();
                        // console.log("删除分类列表结果：",response);
                        let resp = response.data;
                        if (resp.success){
                            _this.all( _this.currentPage);
                            toast.success("删除成功")
                        }
                    }))
                })
